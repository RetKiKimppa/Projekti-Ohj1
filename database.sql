
USE project_03;

DROP TABLE IF EXISTS multiple_choice_answer;
DROP TABLE IF EXISTS multiple_choice_question;
DROP TABLE IF EXISTS question_task;
DROP TABLE IF EXISTS game_save;
DROP TABLE IF EXISTS game_session;
DROP TABLE IF EXISTS airport;
DROP TABLE IF EXISTS player;
DROP TABLE IF EXISTS country;

CREATE TABLE `country` (
  `code` varchar(2) NOT NULL,
  `name` varchar(100) NOT NULL,
  `continent` enum('EU','NA','AS','AF','SA','OC','AN') NOT NULL,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`code`),
  INDEX `idx_country_continent` (`continent`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `airport` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `icao_code` varchar(4) NOT NULL,
  `iata_code` varchar(3) NOT NULL,
  `name` varchar(150) NOT NULL,
  `city` varchar(100) NOT NULL,
  `country_code` varchar(2) NOT NULL,
  `latitude` decimal(10,7) NOT NULL,
  `longitude` decimal(10,7) NOT NULL,
  `elevation_ft` int(11) DEFAULT NULL,
  `continent` varchar(2) NOT NULL,
  `is_major_hub` boolean DEFAULT false,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uq_airport_icao` (`icao_code`),
  UNIQUE KEY `uq_airport_iata` (`iata_code`),
  KEY `idx_airport_country` (`country_code`),
  KEY `idx_airport_hub` (`is_major_hub`),
  FOREIGN KEY (`country_code`) REFERENCES `country` (`code`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `player` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(50) NOT NULL,
  `current_airport_id` int(11) DEFAULT NULL,
  `battery_level` int(11) DEFAULT 100,
  `difficulty_level` enum('easy','medium','hard') DEFAULT 'easy',
  `total_score` int(11) DEFAULT 0,
  `games_played` int(11) DEFAULT 0,
  `games_won` int(11) DEFAULT 0,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  `last_login` timestamp DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_player_current_airport` (`current_airport_id`),
  FOREIGN KEY (`current_airport_id`) REFERENCES `airport` (`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `game_session` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `player_id` int(11) NOT NULL,
  `difficulty_level` enum('easy','medium','hard') NOT NULL DEFAULT 'easy',
  `starting_airport_id` int(11) NOT NULL,
  `boss_airport_id` int(11) NOT NULL,
  `boss_country_code` varchar(2) NOT NULL,
  `current_airport_id` int(11) NOT NULL,
  `battery_level` int(11) DEFAULT 100,
  `puzzles_solved` int(11) DEFAULT 0,
  `countries_guessed` json DEFAULT NULL,
  `status` enum('active','won','lost','abandoned') DEFAULT 'active',
  `score` int(11) DEFAULT 0,
  `started_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  `completed_at` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `idx_game_session_player` (`player_id`),
  KEY `idx_game_session_status` (`status`),
  KEY `idx_game_session_difficulty` (`difficulty_level`),
  FOREIGN KEY (`player_id`) REFERENCES `player` (`id`) ON DELETE CASCADE,
  FOREIGN KEY (`starting_airport_id`) REFERENCES `airport` (`id`),
  FOREIGN KEY (`boss_airport_id`) REFERENCES `airport` (`id`),
  FOREIGN KEY (`current_airport_id`) REFERENCES `airport` (`id`),
  FOREIGN KEY (`boss_country_code`) REFERENCES `country` (`code`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `question_task` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `question` text NOT NULL,
  `correct_answer` varchar(255) NOT NULL,
  `difficulty_level` enum('easy','medium','hard') NOT NULL,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_question_task_difficulty` (`difficulty_level`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `multiple_choice_question` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `question` text NOT NULL,
  `difficulty_level` enum('easy','medium','hard') NOT NULL,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_mc_question_difficulty` (`difficulty_level`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `multiple_choice_answer` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `question_id` int(11) NOT NULL,
  `answer` varchar(255) NOT NULL,
  `is_correct` boolean DEFAULT false,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_mc_answer_question` (`question_id`),
  FOREIGN KEY (`question_id`) REFERENCES `multiple_choice_question` (`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE `game_save` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `player_id` int(11) NOT NULL,
  `save_name` varchar(100) NOT NULL DEFAULT 'autosave',
  `game_data` json NOT NULL,
  `created_at` timestamp DEFAULT CURRENT_TIMESTAMP,
  `updated_at` timestamp DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_game_save_player` (`player_id`),
  FOREIGN KEY (`player_id`) REFERENCES `player` (`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

